package Codiing777;

public class Differents {

    public static void main(String[] args) {
        // Проверяем наши примеры:
        System.out.println("diff21(19) → " + diff21(19)); // Выводим результат для n = 19
        System.out.println("diff21(10) → " + diff21(10)); // Выводим результат для n = 10
    }

    public static int diff21(int n) {
        // Проверяем, больше ли число n, чем 21
        if (n > 21) {
            // Если да, то возвращаем удвоенную абсолютную разницу между n и 21
            return 2 * Math.abs(n - 21);
        } else {
            // Иначе возвращаем абсолютную разницу между n и 21
            return Math.abs(n - 21);
        }
    }
}
   /* Объяснение:

    Мы создаем класс Main, внутри которого будет находиться наша программа.
    В методе main мы вызываем функцию diff21 для различных значений n и выводим результат на экран с помощью System.out.println.
    В методе diff21 мы определяем, какую разницу нужно вернуть, в зависимости от значения числа n:
    Если n больше 21, мы возвращаем удвоенную абсолютную разницу между n и 21, используя return 2 * Math.abs(n - 21).
    В противном случае (если n меньше или равно 21), мы просто возвращаем абсолютную разницу между n и 21 с помощью
    return Math.abs(n - 21).
    Таким образом, программа решает задачу и выводит результаты для примеров: "diff21(19) → 2" и "diff21(10) → 11".*/







