package operators;

public class ComparisonOperators {
    public static void main(String[] args) {


        System.out.println(89>34); // true
        System.out.println(67<100); // true
        System.out.println(78>79);  // false

        System.out.println(88 == 88); //true
        System.out.println(88 != 88); // false ! Знак который делает все наоборот
        System.out.println("-------------------");

        System.out.println(45 >= 23); // true
        System.out.println(67 >= 67); // true
        System.out.println("-------------------");

        System.out.println(56 <= 23); //false
        System.out.println(17 <= 23); //true
        System.out.println(34 <= 34); //true













    }
}
